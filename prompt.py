"""
prompt.py —— Prompt 构造

为什么单独封装：
  - RAG 的第二个命门（第一个是切块）：同样的资料，prompt 写法不同，
    模型可能"老实根据资料答"，也可能"抛开资料自由编"
  - 将来调优 prompt（加 few-shot、改指令语气、A/B 测试），只改这一个文件
  - Agent 升级时，这里会变成"根据历史对话 + 检索结果动态构造 prompt"

设计原则：
  - 输入：检索到的文本块 + 用户问题（+ 未来的对话历史）
  - 输出：完整的 prompt 字符串
  - 明确告诉模型行为边界（只根据资料答 / 没有时怎么办）
"""


def build_prompt(chunks: list[str], question: str, history: list = None) -> str:
    """
    根据检索结果和问题，构造给 LLM 的 prompt。

    参数：
      chunks: 检索到的文本块列表（从 retriever 返回的）
      question: 用户的问题
      history: 对话历史（Agent 伏笔③，当前传 None 占位）

    返回：
      完整的 prompt 字符串

    设计要点（你在 v1 已经做到了，v2 再优化一下）：
      1. 把多个块拼成一段"资料"
      2. 明确指令：只根据资料回答
      3. 边界情况：资料没有答案时，要求模型诚实说明 + 补充业内标准答案
    """
    # ── 第一步：把多个块拼成一段"资料" ──────────────────────────
    # v1 你用的：doc = '\n'.join(chunks)
    # 作用：用换行符把所有块连起来，变成一整段文本
    materials = '\n'.join(chunks)

    # ── 第二步：构造完整的 prompt ──────────────────────────────
    # 你的任务：写一个 f-string，把 materials 和 question 拼进去
    #
    # 参考 v1 你写的（query_rag.py 第 83 行）：
    #   que = f'请解答这个问题{question},注意要只根据以下资料作答，...'
    #
    # v2 可以优化的方向（可选）：
    #   - 用三引号 f"""...""" 写多行，比一行长字符串清晰
    #   - 把指令分成 1. 2. 3. 列出来
    #   - 用分隔符把"资料"和"问题"分开
    #
    # 核心三要素必须有：
    #   ① 只根据资料回答
    #   ② 资料没有答案时诚实说明
    #   ③ 可以补充业内标准答案（但要标注清楚）
    #
    # 下面给你一个起手式，你填内容：

    prompt = f"""
资料内容：
{materials}
回答要求：
1. 只根据上面的资料回答问题
2. 如果资料没有答案，诚实说"资料中没有相关内容"
3. 可以补充业内标准答案，但要标注"以下为补充内容"
问题:{question}
"""

    return prompt


